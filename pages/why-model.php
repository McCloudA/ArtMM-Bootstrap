            <li>Appreciate your body.</li>
            <p>These days people have a lot of weird body image issues. While supermodels and other modeling gigs require 'perfect' bodies, art models are expected to be human. Sometimes anatomy is the focus, and an artist wants a muscular, defined model. Sometimes an artist wants to study different extremes of body types, or be imitating Rubens' work, and they want a fatter, voluptuous model. Most art models are normal people with normal body types - not really fat or muscular.</p>

